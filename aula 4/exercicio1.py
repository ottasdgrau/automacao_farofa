'''
Exercício 1

Crie um programa que recebe como dois números reais. O programa deve imprimir as quatros gravações matemáticas
entre os dois números (soma, subtração, divisão e multiplicação).
'''

print('Digite um valor')
a= int(input())

print('Digite outro valor')
b= int(input())

soma = a+b
subtracao = a-b
divisao = a/b
multiplicacao = a*b

print("A soma desses valores é igual a", soma )
print("A subtração desses valores é igual a", subtracao )
print("A divisão desses valores é igual a", divisao )
print("O produto desses valores é igual a", multiplicacao )